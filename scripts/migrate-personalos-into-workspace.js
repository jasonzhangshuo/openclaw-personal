#!/usr/bin/env node
/**
 * 将旧 personalOS 运行数据迁移到本仓库（单仓运行）。
 *
 * 用法：
 *   node scripts/migrate-personalos-into-workspace.js
 *   node scripts/migrate-personalos-into-workspace.js --source /path/to/personalOS
 *   node scripts/migrate-personalos-into-workspace.js --target /path/to/target
 *   node scripts/migrate-personalos-into-workspace.js --dry-run
 */

const fs = require("fs");
const path = require("path");
const os = require("os");

const repoRoot = path.resolve(__dirname, "..");
const args = process.argv.slice(2);

function readArg(name, fallback = "") {
  const i = args.indexOf(name);
  if (i >= 0 && args[i + 1]) return args[i + 1];
  return fallback;
}

const dryRun = args.includes("--dry-run");
const sourceRoot = readArg("--source", path.join(os.homedir(), "personalOS"));
const targetRoot = readArg(
  "--target",
  path.join(repoRoot, ".openclaw", "workspace-lifecoach", "data", "personalos")
);

const mappings = [
  { from: "data/daily_summary", to: "daily_summary", type: "dir" },
  { from: "data/plan_overrides", to: "plan_overrides", type: "dir" },
  { from: "data/rules", to: "rules", type: "dir" },
  { from: "data/task_events.jsonl", to: "task_events.jsonl", type: "file" },
  { from: "archive/docs/30day.md", to: "docs/30day.md", type: "file" },
];

function ensureDir(dirPath) {
  if (dryRun) return;
  fs.mkdirSync(dirPath, { recursive: true });
}

function copyFileSafe(src, dst) {
  ensureDir(path.dirname(dst));
  if (dryRun) return;
  fs.copyFileSync(src, dst);
}

function copyDirSafe(src, dst) {
  ensureDir(path.dirname(dst));
  if (dryRun) return;
  fs.cpSync(src, dst, { recursive: true, force: true });
}

function main() {
  console.log("[personalOS-migrate] source:", sourceRoot);
  console.log("[personalOS-migrate] target:", targetRoot);
  console.log("[personalOS-migrate] mode:", dryRun ? "dry-run" : "apply");

  ensureDir(targetRoot);

  const report = [];

  for (const item of mappings) {
    const src = path.join(sourceRoot, item.from);
    const dst = path.join(targetRoot, item.to);
    const exists = fs.existsSync(src);

    if (!exists) {
      report.push({ ...item, status: "skipped_missing", src, dst });
      continue;
    }

    if (item.type === "dir") {
      copyDirSafe(src, dst);
      report.push({ ...item, status: "copied_dir", src, dst });
    } else {
      copyFileSafe(src, dst);
      report.push({ ...item, status: "copied_file", src, dst });
    }
  }

  const copied = report.filter((r) => r.status.startsWith("copied")).length;
  const skipped = report.filter((r) => r.status === "skipped_missing").length;

  console.log("\n[personalOS-migrate] result:");
  for (const r of report) {
    console.log(`- ${r.status}: ${r.from} -> ${r.to}`);
  }
  console.log(`\n[personalOS-migrate] copied=${copied}, skipped_missing=${skipped}`);

  if (!dryRun) {
    const markerPath = path.join(targetRoot, "MIGRATION-INFO.md");
    const marker = [
      "# Migration Info",
      "",
      `- Source: \`${sourceRoot}\``,
      `- Target: \`${targetRoot}\``,
      `- Time: ${new Date().toISOString()}`,
      "",
      "This file is generated by scripts/migrate-personalos-into-workspace.js.",
      "",
    ].join("\n");
    fs.writeFileSync(markerPath, marker, "utf8");
    console.log(`[personalOS-migrate] wrote marker: ${markerPath}`);
  }
}

main();
